/*
    이스케이프 시퀀스( escape sequence)
    : \와 함께 사용하는 특수문자
*/

console.log('\0'); // null문자
console.log('\b'); // backspace 문자
console.log('\f'); // formfeed 문자 (타자기 종이를 한 행 말아 올림)
console.log('\n'); // newline 문자 (개행)
console.log('\r'); // carrage return 문자 (타자기 헤드 첫 컬럼으로 이동)
console.log('\t'); // hrizontal tab 문자 (수평 탭)
console.log('\v'); // verical tab (수직 탭)
console.log('\u0061'); // 16진수 4자리 유니코드에 해당하는 문자
console.log('\''); // ' 문자
console.log('\"'); // " 문자
console.log('\\'); // / 문자
